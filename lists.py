names = ["John", "Mosh", "Maria", "Karolaine", "Matheo", "Jenie", "Isa"]
# imprimindo os elementos
print(names)

# consultando os elementos pelo [indice]
print(names[3])
print(names[0])

# indice negativo funciona em Python
print(names[-1])
print(names[-2])

# alterando elemento pelo indice
names [2] = "Karina" 
print(names)

# consultando elementos pelo intervalo dos indices
print(names[3:7])


