# metodos 

numbers = [1, 2, 3, 4, 5]
# adicionar elemento
numbers.append(6)
print(numbers)

# inserir elemento com parametro
numbers.insert(0, -1) # indice e elemento
print(numbers)

# remover elemento
numbers.remove(3)
print(numbers)

# expressao boleana 
print(10 in numbers)

# qtd de elementos
print(len(numbers))

# deixa a lista vazia (clear)
numbers.clear()
print(numbers)

